export function pluralizeLives(n: number) {
  const abs = Math.abs(n);
  if (abs === 1) return '–∂–∏–∑–Ω—å';
  if (abs >= 2 && abs <= 4) return '–∂–∏–∑–Ω–∏';
  return '–∂–∏–∑–Ω–µ–π';
}

export function pluralizeDays(n: number) {
  const abs = Math.abs(n);
  if (abs === 1) return '–¥–µ–Ω—å';
  if (abs >= 2 && abs <= 4) return '–¥–Ω—è';
  return '–¥–Ω–µ–π';
}
